package JavaInterviewQuestions;

public class RightAngleTriangle {

	public static void main(String[] args) {
		
		for (int i=1; i<=5; i++) {      // i goes from 1 to 5, so there will be 5 rows in total.
			
			for(int j=1; j<=i; j++) {   //  now i=2 j=1,2
				
				System.out.print("*");   // printing *
				
			}
			
		System.out.println();    //now to go into next line
			
		}

	}

}
